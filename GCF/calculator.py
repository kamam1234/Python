def add(x,y):
    return x + y

def subtract(x,y):
    return x - y

def multiply(x,y):
    return x * y

def divide(x,y):
    if y == 0:
        return "Cannot divide by zero"
    return x / y

def exponent(x,y):
    return x ** y

def modulus(x,y):
    return x % y

def floor_division(x,y):
    return x // y

def square_root(x):
    return x ** 0.5

def cube_root(x):
    return x ** (1/3)

